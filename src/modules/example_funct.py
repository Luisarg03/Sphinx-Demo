def sum(a, b): 
        """
        Suma dos valores.

        Parameters
        ----------
        a : float
            Primer valor numerico. 
        b: float
            Segundo valor numerico.

        Return
        ------
        float
            Returna un valor numerico.
        """

        c = a + b
        
        return c


def resta(a, b): 
        """
        Resta dos valores.

        Parameters
        ----------
        a : float
            Primer valor numerico. 
        b: float
            Segundo valor numerico.

        Return
        ------
        float
            Returna un valor numerico.
        """

        c = a - b
        
        return c